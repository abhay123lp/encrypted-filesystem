package com.bm.nio.utils;

public class CipherUtilsImplException extends Exception {

	private static final long serialVersionUID = 7934242938374379883L;

	public CipherUtilsImplException() {
		super();
	}

	public CipherUtilsImplException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public CipherUtilsImplException(String message, Throwable cause) {
		super(message, cause);
	}

	public CipherUtilsImplException(String message) {
		super(message);
	}

	public CipherUtilsImplException(Throwable cause) {
		super(cause);
	}
	
}
